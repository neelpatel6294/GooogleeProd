public class CustomConts {

    String mPhoneName;
    String mPhonePrice;

    public CustomConts(String mPhoneName, String mPhonePrice) {
        this.mPhoneName = mPhoneName;
        this.mPhonePrice = mPhonePrice;
    }

    public String getmPhoneName() {
        return mPhoneName;
    }

    public String getmPhonePrice() {
        return mPhonePrice;
    }
}
